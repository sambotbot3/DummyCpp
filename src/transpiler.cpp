namespace dpp {

std::string transpile_bootstrap_subset(const std::string &source) {
  const parser::ParsedSource parsed = parser::parse_translation_unit(source);
  const std::vector<parser::Diagnostic> diagnostics = parser::check_bootstrap_syntax(parsed);
  if (parser::has_errors(diagnostics)) {
    std::ostringstream message;
    message << "syntax check failed";
    for (const parser::Diagnostic &diagnostic : diagnostics) {
      if (diagnostic.level == parser::DiagnosticLevel::error) {
        message << "\n  error: " << diagnostic.message;
      }
    }
    throw std::runtime_error(message.str());
  }

  std::string out = convert::lower_function_templates(parsed).source;
  out = convert::lower_records(out).source;
  convert::VectorResult vector = convert::lower_vectors(out);
  out = vector.source;
  convert::MapResult map = convert::lower_maps(out);
  out = map.source;
  convert::AlgorithmResult algorithm = convert::lower_algorithms(out);
  out = algorithm.source;
  convert::MemoryResult memory = convert::lower_memory(out);
  out = memory.source;
  convert::StringResult string = convert::lower_strings(out);
  out = string.source;
  convert::IostreamResult iostream = convert::lower_iostreams(out);
  out = iostream.source;
  convert::AssertionsResult assertions = convert::lower_assertions(out);
  out = assertions.source;
  out = convert::lower_structs(out);
  out = convert::lower_main_signature(out);
  out = convert::lower_aggregate_initializers(out);

  const std::string includes = iostream.used_stdio ? "#include <stdio.h>\n\n" : "";
  const std::string assert_include = assertions.used_assert ? "#include <assert.h>\n" : "";
  const std::string vector_include = vector.used_vector ? "#include \"dpp_vector.h\"\n" : "";
  const std::string map_include = map.used_map ? "#include \"dpp_map.h\"\n" : "";
  const std::string algorithm_include =
      algorithm.used_algorithm ? "#include \"dpp_algorithm.h\"\n" : "";
  const std::string memory_include = memory.used_memory ? "#include \"dpp_memory.h\"\n" : "";
  const std::string string_include = string.used_string ? "#include \"dpp_string.h\"\n" : "";

  return "/* Generated by DummyCpp/dpp bootstrap transpiler. */\n"
         "/* Supported input is intentionally tiny; see docs/plan.md. */\n\n" +
         includes + assert_include + vector_include + map_include + algorithm_include + memory_include +
         string_include +
         ((vector_include.empty() && map_include.empty() && algorithm_include.empty() &&
           memory_include.empty() && string_include.empty() && assert_include.empty())
              ? ""
              : "\n") +
         out;
}

} // namespace dpp
